package kr.geomex.dataTypeTest;

public class NumericTypeTest {

	public static void main(String[] args) {
	
		byte a =127;
		System.out.println("byte형식의 최댓값은"+a);
		short b= 32767;
		System.out.println("short형식의 최댓값은"+b);
		int c=2147483647;
		System.out.println("int형식의 최댓값은"+c);
		long d= 9223372036854775807L;
		System.out.println("long형식의 최댓값은"+d);
	}

}
